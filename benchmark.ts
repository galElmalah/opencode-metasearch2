#!/usr/bin/env npx tsx

import { spawn, type ChildProcess } from "node:child_process";
import fs from "node:fs";
import path from "node:path";
import { performance } from "node:perf_hooks";

// ---------------------------------------------------------------------------
// Constants (duplicated from index.ts so the benchmark is self-contained)
// ---------------------------------------------------------------------------

const DEFAULT_PORT = 28019;
const STARTUP_TIMEOUT_MS = 30_000; // generous for benchmarking
const HEALTH_POLL_INTERVAL_MS = 200;
const REQUEST_TIMEOUT_MS = 15_000;

const DEFAULT_QUERIES = [
  "typescript performance",
  "nodejs streams",
  "rust vs go",
  "openai api",
  "linux kernel",
];

// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

interface MetasearchSearchResult {
  result: { url: string; title: string; description: string };
  engines: string[];
  score: number;
}

interface MetasearchFeaturedSnippet {
  url: string;
  title: string;
  description: string;
  engine: string;
}

interface MetasearchResponse {
  search_results: MetasearchSearchResult[];
  featured_snippet?: MetasearchFeaturedSnippet;
  answer?: { html: string; engine: string };
}

// ---------------------------------------------------------------------------
// Helpers (duplicated from index.ts)
// ---------------------------------------------------------------------------

function resolveBin(): string | undefined {
  const envBin = process.env.METASEARCH_BIN;
  if (envBin && fs.existsSync(envBin)) return envBin;

  const cargoHome =
    process.env.CARGO_HOME ?? path.join(process.env.HOME ?? "", ".cargo");
  const cargoBin = path.join(cargoHome, "bin", "metasearch");
  if (fs.existsSync(cargoBin)) return cargoBin;

  const tmpBin = "/tmp/metasearch2-build/bin/metasearch";
  if (fs.existsSync(tmpBin)) return tmpBin;

  return undefined;
}

function ensureConfig(configDir: string, port: number): void {
  const configPath = path.join(configDir, "config.toml");
  if (fs.existsSync(configPath)) return;
  fs.mkdirSync(configDir, { recursive: true });
  fs.writeFileSync(
    configPath,
    `# Auto-generated by opencode-metasearch2 benchmark
api = true
bind = "0.0.0.0:${port}"
`,
  );
}

function spawnMetasearch(binPath: string, configDir: string): ChildProcess {
  const child = spawn(binPath, [], {
    env: { ...process.env, XDG_CONFIG_HOME: path.dirname(configDir) },
    stdio: ["ignore", "pipe", "pipe"],
    detached: false,
  });

  child.stdout?.on("data", () => {});
  child.stderr?.on("data", () => {});
  child.on("error", (err) => {
    console.error(`[metasearch2] process error: ${err.message}`);
  });

  return child;
}

async function waitForReady(url: string): Promise<void> {
  const deadline = Date.now() + STARTUP_TIMEOUT_MS;
  while (Date.now() < deadline) {
    try {
      const res = await fetch(`${url}/search?q=ping`, {
        headers: { Accept: "application/json" },
        signal: AbortSignal.timeout(1000),
      });
      if (res.ok) return;
    } catch {
      // not ready yet
    }
    await new Promise((r) => setTimeout(r, HEALTH_POLL_INTERVAL_MS));
  }
  throw new Error(
    `metasearch2 did not become ready within ${STARTUP_TIMEOUT_MS}ms`,
  );
}

// ---------------------------------------------------------------------------
// Stats helpers
// ---------------------------------------------------------------------------

function percentile(sorted: number[], p: number): number {
  if (sorted.length === 0) return 0;
  const idx = (p / 100) * (sorted.length - 1);
  const lo = Math.floor(idx);
  const hi = Math.ceil(idx);
  if (lo === hi) return sorted[lo];
  return sorted[lo] + (sorted[hi] - sorted[lo]) * (idx - lo);
}

function fmtMs(ms: number): string {
  if (ms >= 1000) return `${(ms / 1000).toFixed(2)}s`;
  return `${ms.toFixed(1)}ms`;
}

// ---------------------------------------------------------------------------
// Benchmark runner
// ---------------------------------------------------------------------------

async function runBenchmark(numQueries?: number) {
  const queries = DEFAULT_QUERIES;
  const iterations = numQueries ?? queries.length;

  console.log("=".repeat(64));
  console.log("  metasearch2 benchmark");
  console.log("=".repeat(64));
  console.log();

  // ── Phase 1: Binary resolution ──────────────────────────────────────
  console.log("--- Phase 1: Binary Resolution ---");
  const t0 = performance.now();
  const binPath = resolveBin();
  const resolveTime = performance.now() - t0;

  if (!binPath) {
    console.error(
      "\nERROR: metasearch2 binary not found.\n" +
        "  Searched:\n" +
        "    - METASEARCH_BIN env var\n" +
        "    - ~/.cargo/bin/metasearch\n" +
        "    - /tmp/metasearch2-build/bin/metasearch\n" +
        "\n  Install with: cargo install metasearch\n" +
        "  Or set METASEARCH_BIN to the binary path.\n",
    );
    process.exit(1);
  }

  console.log(`  Binary found: ${binPath}`);
  console.log(`  Resolve time: ${fmtMs(resolveTime)}`);
  console.log();

  // ── Phase 2: Config ─────────────────────────────────────────────────
  console.log("--- Phase 2: Config ---");
  const port = Number(process.env.METASEARCH_PORT) || DEFAULT_PORT;
  const url = `http://localhost:${port}`;
  const configDir = path.join(
    process.env.XDG_CONFIG_HOME ??
      path.join(process.env.HOME ?? "/tmp", ".config"),
    "metasearch",
  );

  const t1 = performance.now();
  ensureConfig(configDir, port);
  const configTime = performance.now() - t1;

  console.log(`  Config dir:   ${configDir}`);
  console.log(`  Config time:  ${fmtMs(configTime)}`);
  console.log();

  // ── Phase 3: Spawn & health check ──────────────────────────────────
  console.log("--- Phase 3: Spawn & Startup ---");
  let child: ChildProcess | undefined;

  const cleanup = () => {
    if (child && !child.killed) {
      child.kill("SIGTERM");
    }
  };
  process.on("exit", cleanup);
  process.on("SIGINT", () => {
    cleanup();
    process.exit(130);
  });
  process.on("SIGTERM", () => {
    cleanup();
    process.exit(143);
  });

  const t2 = performance.now();
  child = spawnMetasearch(binPath, configDir);
  const spawnTime = performance.now() - t2;

  console.log(`  PID:          ${child.pid}`);
  console.log(`  Spawn time:   ${fmtMs(spawnTime)}`);
  console.log(`  Waiting for server to become ready on ${url} ...`);

  const t3 = performance.now();
  try {
    await waitForReady(url);
  } catch (err) {
    console.error(
      `\nERROR: ${err instanceof Error ? err.message : String(err)}`,
    );
    cleanup();
    process.exit(1);
  }
  const readyTime = performance.now() - t3;
  const totalStartup = resolveTime + configTime + spawnTime + readyTime;

  console.log(`  Ready time:   ${fmtMs(readyTime)}`);
  console.log();

  // ── Startup summary ────────────────────────────────────────────────
  console.log("--- Startup Summary ---");
  console.log(`  ${"Phase".padEnd(22)} ${"Duration".padStart(12)}`);
  console.log(`  ${"─".repeat(22)} ${"─".repeat(12)}`);
  console.log(
    `  ${"Binary resolution".padEnd(22)} ${fmtMs(resolveTime).padStart(12)}`,
  );
  console.log(
    `  ${"Config write".padEnd(22)} ${fmtMs(configTime).padStart(12)}`,
  );
  console.log(
    `  ${"Process spawn".padEnd(22)} ${fmtMs(spawnTime).padStart(12)}`,
  );
  console.log(
    `  ${"Health check wait".padEnd(22)} ${fmtMs(readyTime).padStart(12)}`,
  );
  console.log(`  ${"─".repeat(22)} ${"─".repeat(12)}`);
  console.log(
    `  ${"TOTAL".padEnd(22)} ${fmtMs(totalStartup).padStart(12)}`,
  );
  console.log();

  // ── Phase 4: Search benchmark ──────────────────────────────────────
  console.log("--- Phase 4: Search Benchmark ---");
  console.log(`  Running ${iterations} queries...\n`);

  const latencies: number[] = [];
  const results: {
    query: string;
    latencyMs: number;
    resultCount: number;
    error?: string;
  }[] = [];

  for (let i = 0; i < iterations; i++) {
    const query = queries[i % queries.length];
    const params = new URLSearchParams({ q: query });

    const tq = performance.now();
    try {
      const res = await fetch(`${url}/search?${params.toString()}`, {
        headers: { Accept: "application/json" },
        signal: AbortSignal.timeout(REQUEST_TIMEOUT_MS),
      });

      if (!res.ok) {
        const elapsed = performance.now() - tq;
        results.push({
          query,
          latencyMs: elapsed,
          resultCount: 0,
          error: `HTTP ${res.status}`,
        });
        latencies.push(elapsed);
        continue;
      }

      const tabs = (await res.json()) as MetasearchResponse[];
      const elapsed = performance.now() - tq;
      const data = tabs[0];
      const count = data?.search_results?.length ?? 0;

      latencies.push(elapsed);
      results.push({ query, latencyMs: elapsed, resultCount: count });
    } catch (err) {
      const elapsed = performance.now() - tq;
      latencies.push(elapsed);
      results.push({
        query,
        latencyMs: elapsed,
        resultCount: 0,
        error: err instanceof Error ? err.message : String(err),
      });
    }
  }

  // ── Per-query table ────────────────────────────────────────────────
  const qCol = 30;
  const header = `  ${"#".padEnd(4)} ${"Query".padEnd(qCol)} ${"Latency".padStart(12)} ${"Results".padStart(8)} ${"Status".padStart(8)}`;
  console.log(header);
  console.log(
    `  ${"─".repeat(4)} ${"─".repeat(qCol)} ${"─".repeat(12)} ${"─".repeat(8)} ${"─".repeat(8)}`,
  );

  for (let i = 0; i < results.length; i++) {
    const r = results[i];
    const q =
      r.query.length > qCol ? r.query.slice(0, qCol - 3) + "..." : r.query;
    const status = r.error ? `ERR` : "OK";
    console.log(
      `  ${String(i + 1).padEnd(4)} ${q.padEnd(qCol)} ${fmtMs(r.latencyMs).padStart(12)} ${String(r.resultCount).padStart(8)} ${status.padStart(8)}`,
    );
  }

  console.log();

  // ── Aggregate stats ────────────────────────────────────────────────
  if (latencies.length > 0) {
    const sorted = [...latencies].sort((a, b) => a - b);
    const min = sorted[0];
    const max = sorted[sorted.length - 1];
    const avg = latencies.reduce((s, v) => s + v, 0) / latencies.length;
    const p50 = percentile(sorted, 50);
    const p95 = percentile(sorted, 95);

    console.log("--- Search Latency Summary ---");
    console.log(`  ${"Metric".padEnd(12)} ${"Value".padStart(12)}`);
    console.log(`  ${"─".repeat(12)} ${"─".repeat(12)}`);
    console.log(`  ${"Min".padEnd(12)} ${fmtMs(min).padStart(12)}`);
    console.log(`  ${"Max".padEnd(12)} ${fmtMs(max).padStart(12)}`);
    console.log(`  ${"Avg".padEnd(12)} ${fmtMs(avg).padStart(12)}`);
    console.log(`  ${"p50".padEnd(12)} ${fmtMs(p50).padStart(12)}`);
    console.log(`  ${"p95".padEnd(12)} ${fmtMs(p95).padStart(12)}`);
    console.log(`  ${"─".repeat(12)} ${"─".repeat(12)}`);
    console.log(`  ${"Queries".padEnd(12)} ${String(latencies.length).padStart(12)}`);
    console.log(
      `  ${"Errors".padEnd(12)} ${String(results.filter((r) => r.error).length).padStart(12)}`,
    );
  }

  console.log();
  console.log("=".repeat(64));
  console.log("  Benchmark complete");
  console.log("=".repeat(64));

  // ── Cleanup ────────────────────────────────────────────────────────
  cleanup();
  process.exit(0);
}

// ---------------------------------------------------------------------------
// CLI entry point
// ---------------------------------------------------------------------------

const numArg = process.argv[2] ? parseInt(process.argv[2], 10) : undefined;
if (numArg !== undefined && (isNaN(numArg) || numArg < 1)) {
  console.error("Usage: npx tsx benchmark.ts [num_queries]");
  console.error("  num_queries: number of search queries to run (default: 5)");
  process.exit(1);
}

runBenchmark(numArg).catch((err) => {
  console.error(`\nFatal error: ${err instanceof Error ? err.message : String(err)}`);
  process.exit(1);
});
